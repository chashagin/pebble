    public static int[] Glyph(char ch) => Glyphs.TryGetValue(ch, out var g) ? g : Glyphs['?'];

    public static int GlyphWidth(char ch) => Glyph(ch).Length + 1;

    public static int TextWidth(string text)
    {
        double w = 0;
        bool skip = false;
        foreach (char ch in text)
        {
            if (skip) { skip = false; continue; }
            if (ch == '§') { skip = true; continue; }
            w += GlyphWidth(ch);
        }
        return (int)System.Math.Round(w);
    }
